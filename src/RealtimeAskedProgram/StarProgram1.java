package RealtimeAskedProgram;

public class StarProgram1 {

	public static void main(String[] args) {
		for(int a=0;a<=5;a++)
		{
			for(int b=0;b<=a;b++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		System.out.println("======================================");
		//reverse input
		for(int a=5;a>=1;a--)
		{
			for(int b=1;b<=a;b++)
			{
				System.out.print("*");	
			}
			System.out.println();
		}
		System.out.println("====================rajshreepadme===================");
		
		
		
			
	

	}

}
